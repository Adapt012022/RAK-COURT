package com.adapt.rak.main.models;

public class Branch {
	private int id;
	private String name;
	
	
	public Branch() {
		
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	

}
